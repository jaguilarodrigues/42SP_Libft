#include "libft.h"

char		*ft_strtrim(char const *s1, char const *set)
{
	int		i;
	char	*p;

	if (!s1 || !set)
		return (NULL);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	i = ft_strlen(s1);
	while (i && ft_strchr(set, s1[i]))
		i--;
	p = ft_substr((char*)s1, 0, i + 1);
	if (p == NULL)
		return (NULL);
	return (p);
}
